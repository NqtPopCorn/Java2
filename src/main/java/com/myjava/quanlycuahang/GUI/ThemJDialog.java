    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        if (!validateInput()) {
            return;
        }
        int matk = Integer.parseInt(txtMatk.getText());
        String username = txtUsername.getText();
        String pwd = BCrypt.hashpw(String.valueOf(pwdMatkhau.getPassword()), BCrypt.gensalt());
        String vaitro = cbVaiTro.getSelectedItem().toString();
        TaiKhoanDTO tk = new TaiKhoanDTO(matk, username, pwd, vaitro, 1);
        try {
            int rs = TaiKhoanBUS.getInstance().them(tk);
            if (rs != 0) {
                JOptionPane.showMessageDialog(null, "Thêm tài khoản thành công");
                update_result = true;
                this.dispose();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Thêm tài khoản thất bại do lỗi: \n" + e.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            update_result = false;
        }
        
    }//GEN-LAST:event_btnConfirmActionPerformed

