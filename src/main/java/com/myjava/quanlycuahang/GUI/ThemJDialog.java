    private boolean validateInput() {
        if (txtMatk.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mã tài khoản không được để trống");
            return false;
        }
        if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tên đăng nhập không được để trống");
            return false;
        }
        if (pwdMatkhau.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Mật khẩu không được để trống");
            return false;
        }
        if (pwdConfirmpasswd.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Nhập lại mật khẩu không được để trống");
            return false;
        }
        if (!String.valueOf(pwdMatkhau.getPassword()).equals(String.valueOf(pwdConfirmpasswd.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Mật khẩu không trùng khớp");
            return false;
        }
        return true;
    }

