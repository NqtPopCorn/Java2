    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = JDBCUtil.getConnection();
            TaiKhoanDTO tk = TaiKhoanDAO.getInstance().selectById(tfUsername.getText());
            if(tk == null) {
                System.out.println("ten dang nhap sai");
                return;
            }
            String mk = tk.getMatkhau();
            if(BCrypt.checkpw(tfPass.getText(), mk)) {
                System.out.println("Dang nhap thanh cong");
                jPanel1.remove(pnlLogin);
                jPanel1.add(pnlChangepass);
                // changepass(conn);
            }
            else System.out.println("sai mat khau");
            JDBCUtil.closeConnection(conn);
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btLoginActionPerformed

