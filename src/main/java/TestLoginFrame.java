    private void changepass(Connection conn) {
        String sql = "UPDATE taikhoan tk join nhanvien nv on tk.manv=nv.manv SET `matkhau`=? WHERE tk.tendangnhap=?";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, BCrypt.hashpw(tfNewpass2.getText(), BCrypt.gensalt()));
            pst.setString(1, tfUsername.getText());
            pst.executeUpdate();
        } catch(Exception e) {
            System.out.println("Loi doi mat khau");
            e.printStackTrace();
        }
    }

